    function create_vertices(verts_per_side) {
        let procedural_vertices = [];
        for (let i = 0; i < verts_per_side ; i++) {
            for (let j = 0; j < verts_per_side ; j++) {
                procedural_vertices.push([-1 + (2 / verts_per_side)*i, -1 + (2 / verts_per_side)*j, 0]);
            }
        }

        return procedural_vertices;
    }


    function create_faces(verts_per_side) {

        // if there's x verts per side, then there's x-1 spaces in-between, so
        // there are (x-1)^2 total squares enclosed which makes ((x-1)^2)*2 triangles
        // the first triangle uses vertices 1, 0 and x. The complementary triangle to that
        // uses 1, x and x+1. Then the next two are [2, 1, x+1] and [2, x+1, x+2]
        // the last ones are [x-1, x-2, 2x-2] and [x-1, 2x-2, 2x-1]

        let procedural_faces = [];
        let offset;
        for (let i = 0; i < verts_per_side-1; i++) {
            offset = verts_per_side*i;
            for (let j = 0; j < verts_per_side-1; j++) {
                procedural_faces.push([offset + j+1, offset + j, (offset+verts_per_side) + j]);
                procedural_faces.push([offset + j+1, (offset+verts_per_side) + j, (offset+verts_per_side) + j+1]);
            }
        }

        return procedural_faces;
    }
